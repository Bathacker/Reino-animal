public class PezAnormal extends Pez
{
	
	private String col, tam;

	public void setColor(String color)
	{

		col = color;

	}

	public void setTamano(String tamano)
	{

		tam = tamano;

	}

	public String getColor()
	{

		return col;

	}

	public String getTamano()
	{

		return tam;

	}

	public void respirar()
	{

		super.respirar();
		
	}

}